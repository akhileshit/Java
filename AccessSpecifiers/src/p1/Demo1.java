public class Demo1 { // package 1
	
	public int a=10;
	
	protected int b=20;
	
		int c=30;
		
	private int d=40;
	
	
	
	void display1() {
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}
